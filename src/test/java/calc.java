public class calc {
    public static void main(String args[]){

        int x = 19;
        int y = 2;
        int v = 7;
        int c = ++v; // increment
        int b = --v; //decrement
        x = x + y;
        System.out.println(c);
        System.out.println(x);
        System.out.println(b);
    }
}